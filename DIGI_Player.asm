;         STA flag               ;
exit     PLA                    ; restore state
         TAY                    ;
         PLA                    ;
         TAX                    ;
         PLA                    ;
         RTI                    ; return from this pass of NMI

         ; Sample's lower nybble holds the 4-bit sample to played on the
